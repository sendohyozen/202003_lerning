# 单引号用法
print(1314)
print('哪吒') # print(哪吒) 报错，打印引号内容，无引号是变量名

# 双引号用法
# print()函数不仅能够穿一件黄袍(一对单引号)，还能穿两件，也就是使用双引号，其实穿一件与穿两件没什么区别。都能打印出文本。
#括号内有单引号也有双引号
print("let's go go go!")

# 三引号用法
# 在print()函数中使用三引号'''实现跨行输出。
print('''哪吒，
托塔李天王家的三太子，
变化三头六臂又或三头八臂，
百邪不侵专克摄魂夺魄的莲花化身''')

